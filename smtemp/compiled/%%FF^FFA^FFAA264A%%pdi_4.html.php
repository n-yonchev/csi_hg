				Чл.44.(1) Връчителят удостоверява с подписа си датата и начина на връщането, както и всички действия във връзка с връчването. 
				Той отбелязва и качеството на лицето на което е връчено съобщението. Отказът да се приеме съобщението се отбелязва в разписката и 
				се удостоверява с подписа на връчителя. Отказът на получателя не засяга редовността на връчването. Чл.47.(1) Когато ответникът не 
				може да бъде намерен на посочения по делото адрес и не се намери лице, което е съгласно да получи съобщението, връчителят залепва 
				уведомление на вратата или на пощенската кутия, а когато до тях не е осигурен достъп - на входната врата или на видно място около 
				нея. Когато има достъп до пощенската кутия, връчителят пуска уведомление и в нея.<br />
				Чл.51.(3) Адвокатът не може да откаже получаването на съобщението на своя доверител, освен след оттегляне на пълномощното по 
				реда на чл.35, отказ от пълномощие по чл.36, както и когато от пълномощното недвусмислено личи, че не се отнася за инстанцията, 
				за която е призоваването. Отказът на адвоката да приеме съобщението се отбелязва в разписката и се удостоверява с подписа на 
				връчителя. Отказът не засяга редовността на връчването.<br />
