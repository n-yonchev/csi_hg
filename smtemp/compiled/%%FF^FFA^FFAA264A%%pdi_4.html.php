				ЗАКОН ЗА ПРАВНАТА ПОМОЩ
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.5.</b> Правна помощ се предоставя на физически лица на основанията, посочени в този и други закони.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.23.</b> (1) Системата за правна помощ по чл.21, т.З обхваща случаите, при които по силата на закон задължително се предвижда адвокатска защита или представителство.
				<br />(2) Системата за правна помощ обхваща и случаите, когато заподозреният, обвиняемият, уличеният, подсъдимият или страната по наказателно, гражданско или административно дело не разполага със средства за заплащане на адвокат, желае да има такъв и интересите на правосъдието изискват това.
				<br />(4) По граждански и административни дела правна помощ се предоставя в случаите, когато въз основа на представени 
				доказателства от съответните компетентни органи съдът прецени, че страната няма средства за заплащане на адвокатското 
				възнаграждение. Преценката си за това съдът формира, като вземе предвид: 1. доходите на лицето и семейството; 2. имущественото 
				състояние, удостоверено с декларация; 3. семейното положение; 4. здравословното състояние; 5. трудовата заетост; 6. възрастта и 7. други констатирани обстоятелства. 
