				ГРАЖДАНСКИ ПРОЦЕСУАЛЕН КОДЕКС<br />
				Съдебен адресат <br />
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.40.</b> (1) Страната, която живее или замине за повече от един месец в чужбина, 
				е длъжна да посочи лице в седалището на съда, на което да се връчат съобщенията - съдебен адресат, ако няма пълномощник в Република България. Същото задължение имат законния представител, попечителят и пълномощникът на страната.
				<br />(2) Когато лицата по ал. 1 не посочат съдебен адресат, всички съобщения се прилагат към делото и се смятат за връчени. 
				За тези последици те трябва да бъдат предупредени от съда при връчване на първото съобщение. Задължение за уведомяване
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.41.</b>(1). Страната, която е отсъства повече от един месец от адреса, който е 
				съобщила по делото или на който веднъж и е връчено съобщение, е длъжна да уведоми съда за новия си адрес. Същото задължение имат и законният представител, попечителят или пълномощникът на страната.
				<br />(2). При неизпълнение на задължението по ал.1, всички съобщения се прилагат към делото и се смятат за връчени. За тези 
				последици страната трябва да бъде предупредена от съда при връчване на първото съобщение. Връчване на търговци и юридически лица
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.50.</b>(1) Мястото на връчване на търговец и на юридеческо лице, което е вписано в 
				съответния регистър, е последният посочен в регистъра адрес.
				<br />(2). Ако лицето е напуснало адреса си, и в регистъра не е вписан новия му адрес, всички съобщения се прилагат по делото 
				и се смятат за редовно връчени. Покана за добромолно изпълнение
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.428.</b>(1) Съдебният изпълнител е длъжен да покани длъжника да изпълни доброволно 
				задължението си в двуседмични срок. Когато пристъпва към изпълнение въз основа на заповед за изпълнение, съдебния изпълнител кани 
				длъжника с връчването й, а когато заповедта е била връчена на длъжника, нов срок за доброволното изпълнение не се дава. Действие на запора и на възбраната по отношение на длъжника
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.451.</b>(1) От момента на налагане на запора, длъжникът се лишава от правото 
				да се разпорежда с вземането си или с вещта и не може под страха от наказателна отговорност да изменя, поврежда или унищожава вещта.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(2) Последиците по ал. 1 настъпват за длъжника от момента на получаване на поканата за доброволно изпълнение, когато 
				изпълнението е насочено върху движима или недвижима вещ и тази вещ е посочена в поканата. Изпълнение на задължение на заместимо действие
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.526.</b>(1) Когато длъжникът не изпълни едно действие, което е осъден да извърши и което действие може да бъде извършено 
				от друго лице, взискателят може да иска от съдебния изпълнител да го оправомощи да извърши действието за сметка на длъжника.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(2) Взискателят може да поиска от съда длъжникът да бъде осъден да внесе предварително сумата, 
				която е необходима за извършване на действието. Изпълнение на задължение за незаместимо действие и за бездействие
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>Чл.527.</b>(1) Когато действието не може да се извърши от друго лице, а зависи 
				изключително от волята на длъжника, съдебният изпълнител по искане на взискателя принуждава длъжника да извърши действието, 
				като му налага глоба до 200 лв. Ако и след това длъжникът не извърши действието, съдебният изпълнител му налага последователно 
				нови глоби до същия размер.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(2) Правилото по ал.1 не се прилага за задълженията на работници и служители, които произтичат от трудово или служебно правоотношение.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(3) Когато длъжникът върши противното на това което с решението е задължен да върши или да търпи, съдебният изпълнител по искане на взискателя му налага за всяко нарушение на това задължение глоба до 400 лв.
				<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(4)	Действията на съдебния изпълнител по оправомощаването и по налагане на глобите подлежат на обжалване по реда на чл.435-438.
